a = 5
b = 10

print(a == b)   # Equal to → False
print(a != b)   # Not equal → True
print(a > b)    # Greater than → False
print(a < b)    # Less than → True
print(a >= b)   # Greater than or equal → False
print(a <= b)   # Less than or equal → True
